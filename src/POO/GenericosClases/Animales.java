package src.POO.GenericosClases;

public class Animales {
    private String nombre;
    private String tipo;

    public Animales(String nombre, String tipo) {
        this.nombre = nombre;
        this.tipo = tipo;
    }

    public String getNombre() {
        return nombre;
    }

    public String getTipo() {
        return tipo;
    }

}
