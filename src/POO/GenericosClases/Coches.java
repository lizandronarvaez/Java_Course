package src.POO.GenericosClases;

public class Coches {

    private String marca;
    private String modelo;

    public Coches(String marca, String modelo) {
        this.marca = marca;
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public String getModelo() {
        return modelo;
    }

}
