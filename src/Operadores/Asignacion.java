package src.Operadores;


public class Asignacion {
    public static void main(String[] args) {
        int i = 10;
        // incremento
        i += 4;
        i++;

        // decremeto
        i -= 10;
        i--;

        // Multiplicacion
        i *= 2;

        // divison
        i /= 4;

        // Sout
        System.out.println(i);
    }
}
