package Java_Curso.src.Variables;
public class Primitivos {
    public static void main(String[] args) {

        // BYTES
        byte numeroByte = 127;
        System.out.println(numeroByte);
        System.out.println("Tipo byte corresponde en byte a: " + Byte.BYTES);
        System.out.println("Tipo byte corresponde en bites a: " + Byte.SIZE);
        System.out.println("Valor maximo de byte: " + Byte.MAX_VALUE);
        System.out.println("Valor minimo de byte: " + Byte.MIN_VALUE);
        System.out.println("");

        // short
        short numeroShort = 32767;
        System.out.println(numeroShort);
        System.out.println("Tipo byte corresponde en byte a: " + Short.BYTES);
        System.out.println("Tipo byte corresponde en bites a: " + Short.SIZE);
        System.out.println("Valor maximo de byte: " + Short.MAX_VALUE);
        System.out.println("Valor minimo de byte: " + Short.MIN_VALUE);
        System.out.println("");

        // INT
        int numeroInt = 2147483647;
        System.out.println(numeroInt);
        System.out.println("Tipo byte corresponde en byte a: " + Integer.BYTES);
        System.out.println("Tipo byte corresponde en bites a: " + Integer.SIZE);
        System.out.println("Valor maximo de byte: " + Integer.MAX_VALUE);
        System.out.println("Valor minimo de byte: " + Integer.MIN_VALUE);
        System.out.println("");

        // LONG
        long numeroLong = 9223372036854775807L;
        System.out.println(numeroLong);
        System.out.println("Tipo byte corresponde en byte a: " + Long.BYTES);
        System.out.println("Tipo byte corresponde en bites a: " + Long.SIZE);
        System.out.println("Valor maximo de byte: " + Long.MAX_VALUE);
        System.out.println("Valor minimo de byte: " + Long.MIN_VALUE);
        System.out.println("");

    }
}
